/// Convert a single cell from an Arrow array to its Avro `Value`
/// representation.
///
/// # Parameters
/// - `array`            — The Arrow column array.
/// - `field`            — Arrow field descriptor (name, type, nullability).
/// - `avro_field_schema`— The *unwrapped* Avro schema for this field
///   (nullable unions already peeled to their inner type).  Used to
///   select the correct `AvroValue` variant for types whose Arrow→Avro
///   mapping is ambiguous (e.g. `Timestamp` may map to `Long`,
///   `TimestampMillis`, or `TimestampMicros` depending on what
///   `arrow-avro` emitted).
/// - `row_index`        — Row within the array.
fn arrow_value_to_avro_value(
    array: &dyn Array,
    field: &Field,
    avro_field_schema: &AvroSchemaDef,
    row_index: usize,
    is_avro_union: bool,
) -> Result<AvroValue> {
    // Use is_avro_union (derived from the actual Avro schema structure) rather
    // than field.is_nullable() (Arrow nullability) to decide Union wrapping.
    // arrow-avro 58.x emits ["null", T] Union schemas for Timestamp fields
    // even when the Arrow field is declared non-nullable, so Arrow nullability
    // alone is not a reliable signal for whether to produce AvroValue::Union.
    if array.is_null(row_index) {
        return if is_avro_union {
            Ok(AvroValue::Union(0, Box::new(AvroValue::Null)))
        } else {
            Ok(AvroValue::Null)
        };
    }

    let raw =
        match field.data_type() {
            // -------------------------------------------------------------------
            // String types
            // -------------------------------------------------------------------
            DataType::Utf8 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<StringArray>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected StringArray for field {}", field.name())
                    })?;
                AvroValue::String(arr.value(row_index).to_string())
            }
            DataType::LargeUtf8 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<LargeStringArray>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected LargeStringArray for field {}", field.name())
                    })?;
                AvroValue::String(arr.value(row_index).to_string())
            }

            // -------------------------------------------------------------------
            // Signed integer types
            // -------------------------------------------------------------------
            DataType::Int8 => {
                let arr = array.as_any().downcast_ref::<Int8Array>().ok_or_else(|| {
                    anyhow::anyhow!("Expected Int8Array for field {}", field.name())
                })?;
                AvroValue::Int(arr.value(row_index) as i32)
            }
            DataType::Int16 => {
                let arr = array.as_any().downcast_ref::<Int16Array>().ok_or_else(|| {
                    anyhow::anyhow!("Expected Int16Array for field {}", field.name())
                })?;
                AvroValue::Int(arr.value(row_index) as i32)
            }
            DataType::Int32 => {
                let arr = array.as_any().downcast_ref::<Int32Array>().ok_or_else(|| {
                    anyhow::anyhow!("Expected Int32Array for field {}", field.name())
                })?;
                AvroValue::Int(arr.value(row_index))
            }
            DataType::Int64 => {
                let arr = array.as_any().downcast_ref::<Int64Array>().ok_or_else(|| {
                    anyhow::anyhow!("Expected Int64Array for field {}", field.name())
                })?;
                AvroValue::Long(arr.value(row_index))
            }

            // -------------------------------------------------------------------
            // Unsigned integer types — mapped to Avro Long (no unsigned in Avro)
            // -------------------------------------------------------------------
            DataType::UInt8 => {
                let arr = array.as_any().downcast_ref::<UInt8Array>().ok_or_else(|| {
                    anyhow::anyhow!("Expected UInt8Array for field {}", field.name())
                })?;
                AvroValue::Long(arr.value(row_index) as i64)
            }
            DataType::UInt16 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<UInt16Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected UInt16Array for field {}", field.name())
                    })?;
                AvroValue::Long(arr.value(row_index) as i64)
            }
            DataType::UInt32 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<UInt32Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected UInt32Array for field {}", field.name())
                    })?;
                AvroValue::Long(arr.value(row_index) as i64)
            }
            DataType::UInt64 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<UInt64Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected UInt64Array for field {}", field.name())
                    })?;
                AvroValue::Long(arr.value(row_index) as i64)
            }

            // -------------------------------------------------------------------
            // Floating-point types
            // -------------------------------------------------------------------
            DataType::Float32 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<Float32Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected Float32Array for field {}", field.name())
                    })?;
                AvroValue::Float(arr.value(row_index))
            }
            DataType::Float64 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<Float64Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected Float64Array for field {}", field.name())
                    })?;
                AvroValue::Double(arr.value(row_index))
            }

            // -------------------------------------------------------------------
            // Boolean
            // -------------------------------------------------------------------
            DataType::Boolean => {
                let arr = array
                    .as_any()
                    .downcast_ref::<BooleanArray>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected BooleanArray for field {}", field.name())
                    })?;
                AvroValue::Boolean(arr.value(row_index))
            }

            // -------------------------------------------------------------------
            // Date32 → Avro `date` logical type (int, days since Unix epoch).
            //
            // arrow-avro reliably emits `{"type":"int","logicalType":"date"}` for
            // Date32, so apache-avro parses it as `Schema::Date` and we can
            // unconditionally use `AvroValue::Date`.
            // -------------------------------------------------------------------
            DataType::Date32 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<Date32Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected Date32Array for field {}", field.name())
                    })?;
                AvroValue::Date(arr.value(row_index))
            }

            // -------------------------------------------------------------------
            // Date64 — Arrow stores milliseconds since epoch.
            //
            // arrow-avro may emit different Avro types; dispatch on the actual
            // parsed schema to produce the matching AvroValue.
            //
            //   Schema::TimestampMillis → AvroValue::TimestampMillis(ms)
            //   Schema::TimestampMicros → AvroValue::TimestampMicros(ms × 1000)
            //   Schema::Date            → AvroValue::Date(ms / 86_400_000)
            //   Schema::Long (fallback) → AvroValue::Long(ms)
            // -------------------------------------------------------------------
            DataType::Date64 => {
                let arr = array
                    .as_any()
                    .downcast_ref::<Date64Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected Date64Array for field {}", field.name())
                    })?;
                let millis = arr.value(row_index);
                match avro_field_schema {
                    AvroSchemaDef::TimestampMillis => AvroValue::TimestampMillis(millis),
                    AvroSchemaDef::TimestampMicros => {
                        AvroValue::TimestampMicros(millis.saturating_mul(1_000))
                    }
                    AvroSchemaDef::Date => AvroValue::Date((millis / 86_400_000) as i32),
                    _ => AvroValue::Long(millis),
                }
            }

            // -------------------------------------------------------------------
            // Timestamp — all four Arrow time-units supported.
            //
            // arrow-avro may or may not emit logicalType annotations, so we
            // dispatch on the *actual* Avro schema rather than assuming it:
            //
            //   Schema::TimestampMillis → AvroValue::TimestampMillis(value_in_ms)
            //   Schema::TimestampMicros → AvroValue::TimestampMicros(value_in_µs)
            //   Schema::Long (fallback) → AvroValue::Long(native Arrow value)
            //
            // For the Long fallback, we preserve the native Arrow resolution
            // (seconds / ms / µs / ns) unchanged so the producer and any
            // schema-aware consumer can agree on units via out-of-band convention.
            //
            // Sub-microsecond precision is truncated when upscaling nanoseconds
            // to microseconds (÷ 1 000).
            // -------------------------------------------------------------------
            DataType::Timestamp(unit, _) => {
                encode_timestamp(array, field, unit, avro_field_schema, row_index)?
            }

            // -------------------------------------------------------------------
            // Binary types → Avro `bytes`
            //
            // All three Arrow binary variants produce `AvroValue::Bytes`.
            // FixedSizeBinary length is not re-validated here; the Avro schema
            // produced by arrow-avro uses variable-length `bytes`.
            // -------------------------------------------------------------------
            DataType::Binary => {
                let arr = array
                    .as_any()
                    .downcast_ref::<BinaryArray>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected BinaryArray for field {}", field.name())
                    })?;
                AvroValue::Bytes(arr.value(row_index).to_vec())
            }
            DataType::LargeBinary => {
                let arr = array
                    .as_any()
                    .downcast_ref::<LargeBinaryArray>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected LargeBinaryArray for field {}", field.name())
                    })?;
                AvroValue::Bytes(arr.value(row_index).to_vec())
            }
            DataType::FixedSizeBinary(_) => {
                let arr = array
                    .as_any()
                    .downcast_ref::<FixedSizeBinaryArray>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected FixedSizeBinaryArray for field {}", field.name())
                    })?;
                AvroValue::Bytes(arr.value(row_index).to_vec())
            }

            // -------------------------------------------------------------------
            // Decimal128 → Avro `decimal` logical type
            //
            // Arrow Decimal128 stores the unscaled i128 integer value.  Avro
            // `decimal` encodes the unscaled value as big-endian two's-complement
            // bytes.  We use a minimal encoding (fewest bytes that still preserve
            // the value and sign) to keep payload sizes small.
            // -------------------------------------------------------------------
            DataType::Decimal128(_precision, _scale) => {
                let arr = array
                    .as_any()
                    .downcast_ref::<Decimal128Array>()
                    .ok_or_else(|| {
                        anyhow::anyhow!("Expected Decimal128Array for field {}", field.name())
                    })?;
                let unscaled: i128 = arr.value(row_index);
                let be_bytes = i128_to_avro_bytes(unscaled);
                AvroValue::Decimal(apache_avro::Decimal::from(be_bytes))
            }

            // -------------------------------------------------------------------
            // Unsupported — surface a structured error so the sink can map it to
            // SinkError::UnsupportedType / UnsupportedTypeError in Python.
            // -------------------------------------------------------------------
            dt => {
                return Err(anyhow::anyhow!(
                    "Unsupported Arrow data type {:?} for field {} in Avro conversion",
                    dt,
                    field.name()
                ));
            }
        };

    // Wrap in a Union when the Avro field schema is a Union type:
    //   Union(0, Null)  — null branch (handled at the top of this function)
    //   Union(1, value) — value branch
    if is_avro_union {
        Ok(AvroValue::Union(1, Box::new(raw)))
    } else {
        Ok(raw)
    }
}

// ---------------------------------------------------------------------------
// Timestamp encoding — extracted to keep the match arm readable
// ---------------------------------------------------------------------------

/// Encode a single Timestamp cell as the `AvroValue` that matches the
/// *actual* Avro field schema (which may or may not carry a logicalType).
///
/// # Why this function exists
/// `arrow-avro` 58.x does not always emit logicalType annotations for
/// Timestamp Arrow types.  Concretely, it can generate `{"type":"long"}`
/// instead of `{"type":"long","logicalType":"timestamp-micros"}`.  When
/// apache-avro parses the schema as `Schema::Long`, passing
/// `AvroValue::TimestampMicros` fails validation ("Value does not match
/// schema").  We therefore read the *actual* parsed Avro schema to decide
/// which `AvroValue` variant to produce.
#[inline]
fn encode_timestamp(
    array: &dyn Array,
    field: &Field,
    unit: &TimeUnit,
    avro_field_schema: &AvroSchemaDef,
    row_index: usize,
) -> Result<AvroValue> {
    // Step 1: extract the raw i64 value at the Arrow native resolution.
    let raw: i64 = match unit {
        TimeUnit::Second => array
            .as_any()
            .downcast_ref::<TimestampSecondArray>()
            .ok_or_else(|| {
                anyhow::anyhow!("Expected TimestampSecondArray for field {}", field.name())
            })?
            .value(row_index),
        TimeUnit::Millisecond => array
            .as_any()
            .downcast_ref::<TimestampMillisecondArray>()
            .ok_or_else(|| {
                anyhow::anyhow!(
                    "Expected TimestampMillisecondArray for field {}",
                    field.name()
                )
            })?
            .value(row_index),
        TimeUnit::Microsecond => array
            .as_any()
            .downcast_ref::<TimestampMicrosecondArray>()
            .ok_or_else(|| {
                anyhow::anyhow!(
                    "Expected TimestampMicrosecondArray for field {}",
                    field.name()
                )
            })?
            .value(row_index),
        TimeUnit::Nanosecond => array
            .as_any()
            .downcast_ref::<TimestampNanosecondArray>()
            .ok_or_else(|| {
                anyhow::anyhow!(
                    "Expected TimestampNanosecondArray for field {}",
                    field.name()
                )
            })?
            .value(row_index),
    };

    // Step 2: convert `raw` to the resolution expected by the Avro schema.
    //
    // Conversion table (rows = Arrow unit, columns = target Avro type):
    //
    //   Arrow unit  │ TimestampMillis │ TimestampMicros │ Long (fallback)
    //   ────────────┼─────────────────┼─────────────────┼─────────────────
    //   Second      │ raw × 1 000     │ raw × 1 000 000 │ raw (seconds)
    //   Millisecond │ raw             │ raw × 1 000     │ raw (ms)
    //   Microsecond │ raw / 1 000     │ raw             │ raw (µs)
    //   Nanosecond  │ raw / 1 000 000 │ raw / 1 000     │ raw (ns)
    //
    // Sub-unit precision is truncated (integer division), not rounded.
    Ok(match avro_field_schema {
        AvroSchemaDef::TimestampMillis | AvroSchemaDef::LocalTimestampMillis => {
            let ms = match unit {
                TimeUnit::Second => raw.saturating_mul(1_000),
                TimeUnit::Millisecond => raw,
                TimeUnit::Microsecond => raw / 1_000,
                TimeUnit::Nanosecond => raw / 1_000_000,
            };
            AvroValue::TimestampMillis(ms)
        }
        AvroSchemaDef::TimestampMicros | AvroSchemaDef::LocalTimestampMicros => {
            let us = match unit {
                TimeUnit::Second => raw.saturating_mul(1_000_000),
                TimeUnit::Millisecond => raw.saturating_mul(1_000),
                TimeUnit::Microsecond => raw,
                TimeUnit::Nanosecond => raw / 1_000,
            };
            AvroValue::TimestampMicros(us)
        }
        // Fallback: the Avro schema carries no logicalType (Schema::Long).
        // Preserve the value at its native Arrow resolution so schema-aware
        // consumers can apply their own interpretation.
        _ => AvroValue::Long(raw),
    })
}

// ---------------------------------------------------------------------------
// Decimal128 helper
// ---------------------------------------------------------------------------

/// Convert an `i128` to the minimal big-endian two's-complement byte slice
/// required by the Avro `decimal` logical type.
///
/// "Minimal" means the leading byte is the smallest prefix that still
/// unambiguously represents the sign:
/// - Positive: no leading `0x00` bytes, unless the next byte would have its
///   high bit set (which would misrepresent it as negative).
/// - Negative: no leading `0xFF` bytes, unless the next byte has its high bit
///   clear (which would misrepresent it as positive).
///
/// Examples:
/// ```text
///  0        → [0x00]
///  1        → [0x01]
/// -1        → [0xFF]
///  127      → [0x7F]
///  128      → [0x00, 0x80]   ← needs leading 0x00 to stay positive
/// -128      → [0x80]
/// -129      → [0xFF, 0x7F]
/// ```
fn i128_to_avro_bytes(value: i128) -> Vec<u8> {
    let be = value.to_be_bytes(); // 16-byte big-endian two's complement
    let mut start = 0usize;

    if value >= 0 {
        while start < 15 && be[start] == 0x00 && (be[start + 1] & 0x80) == 0 {
            start += 1;
        }
    } else {
        while start < 15 && be[start] == 0xFF && (be[start + 1] & 0x80) != 0 {
            start += 1;
        }
    }

    be[start..].to_vec()
}

