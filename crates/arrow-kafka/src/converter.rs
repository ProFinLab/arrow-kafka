use anyhow::{Context, Result};
use apache_avro::{Schema as AvroSchemaDef, types::Value as AvroValue};
use arrow::array::*;
use arrow::datatypes::{DataType, Field, SchemaRef, TimeUnit};
use arrow::record_batch::RecordBatch;
use arrow_avro::schema::AvroSchema;
use std::convert::TryFrom;

// ---------------------------------------------------------------------------
// ArrowToAvroConverter
// ---------------------------------------------------------------------------

pub struct ArrowToAvroConverter {
    avro_schema: AvroSchemaDef,
    arrow_schema: SchemaRef,
    schema_json: String,
    schema_name: String,
    /// Per-field "inner" Avro schemas: for nullable fields the union is
    /// unwrapped so callers see the concrete type (e.g. `Schema::Long`),
    /// not `Schema::Union([Null, Long])`.
    ///
    /// Used by `arrow_value_to_avro_value` to dispatch the correct
    /// `AvroValue` variant when the arrow-avro crate omits logicalType
    /// annotations (e.g. emitting `{"type":"long"}` instead of
    /// `{"type":"long","logicalType":"timestamp-micros"}`).
    field_avro_schemas: Vec<AvroSchemaDef>,
    /// Whether each field's top-level Avro schema is a Union type.
    ///
    /// `arrow-avro` 58.x emits `["null", T]` Union schemas for some Arrow
    /// types (notably `Timestamp`) even when the Arrow field is non-nullable.
    /// This flag captures the *actual Avro structure* so that
    /// `arrow_value_to_avro_value` can correctly wrap values in
    /// `AvroValue::Union(1, …)` regardless of Arrow nullability.
    field_is_avro_union: Vec<bool>,
}

impl ArrowToAvroConverter {
    pub fn new(arrow_schema: SchemaRef) -> Result<Self> {
        let avro_schema_wrapper = AvroSchema::try_from(arrow_schema.as_ref())
            .map_err(|e| anyhow::anyhow!("Arrow to Avro conversion failed: {}", e))?;

        let avro_schema = AvroSchemaDef::parse_str(&avro_schema_wrapper.json_string)
            .context("Failed to parse the generated Avro JSON schema")?;

        let schema_name = match &avro_schema {
            AvroSchemaDef::Record(r) => r.name.name.clone(),
            _ => "record".to_string(),
        };

        // Extract the concrete (non-union) Avro schema for each field so
        // that value encoding can consult the real schema type rather than
        // assuming which logicalType the arrow-avro crate emitted.
        let field_avro_schemas = extract_field_inner_schemas(&avro_schema);

        // Track which fields have a Union Avro schema.  arrow-avro 58.x
        // wraps some types (notably Timestamp) in ["null", T] even when the
        // Arrow field is non-nullable, so we use Avro structure — not Arrow
        // nullability — to decide whether to emit AvroValue::Union wrappers.
        let field_is_avro_union: Vec<bool> = match &avro_schema {
            AvroSchemaDef::Record(r) => r
                .fields
                .iter()
                .map(|f| matches!(f.schema, AvroSchemaDef::Union(_)))
                .collect(),
            _ => vec![],
        };

        Ok(Self {
            avro_schema,
            arrow_schema,
            schema_json: avro_schema_wrapper.json_string,
            schema_name,
            field_avro_schemas,
            field_is_avro_union,
        })
    }

    pub fn schema_json(&self) -> &str {
        &self.schema_json
    }

    /// Return the Avro record name as derived from the converted schema.
    ///
    /// For Arrow schemas this is the `"name"` field of the top-level Avro
    /// record object (e.g. `"arrow_schema"` as generated by `arrow-avro`).
    /// Used by [`crate::schema_registry::SubjectNameStrategy`] to compute
    /// the Schema Registry subject for `RecordName` and `TopicRecordName`
    /// strategies.
    pub fn schema_name(&self) -> &str {
        &self.schema_name
    }

    pub fn convert_row(&self, batch: &RecordBatch, row_index: usize) -> Result<Vec<u8>> {
        if row_index >= batch.num_rows() {
            return Err(anyhow::anyhow!(
                "row_index {} out of bounds for batch with {} rows",
                row_index,
                batch.num_rows()
            ));
        }

        let mut fields = Vec::with_capacity(batch.num_columns());

        for (col_idx, field) in self.arrow_schema.fields().iter().enumerate() {
            let column = batch.column(col_idx);
            // Fall back to Schema::Null if field_avro_schemas is shorter than
            // the arrow schema (should never happen for well-formed inputs).
            let avro_field_schema = self
                .field_avro_schemas
                .get(col_idx)
                .unwrap_or(&AvroSchemaDef::Null);
            // Use the Avro-schema-derived union flag rather than Arrow's
            // is_nullable() — see field_is_avro_union doc for the rationale.
            let is_avro_union = self
                .field_is_avro_union
                .get(col_idx)
                .copied()
                .unwrap_or(false);
            let value = arrow_value_to_avro_value(
                column.as_ref(),
                field,
                avro_field_schema,
                row_index,
                is_avro_union,
            )?;
            fields.push((field.name().clone(), value));
        }

        let record = AvroValue::Record(fields);
        let buffer = apache_avro::to_avro_datum(&self.avro_schema, record)
            .context("Failed to encode pure Avro datum")?;

        Ok(buffer)
    }
}

// ---------------------------------------------------------------------------
// Per-field schema extraction
// ---------------------------------------------------------------------------

/// For each field of the top-level Avro Record schema, extract the
/// "concrete" inner schema — i.e. unwrap nullable unions so callers
/// see `Schema::Long` instead of `Schema::Union([Null, Long])`.
///
/// The resulting `Vec` is index-aligned with the Arrow schema's field list
/// (and with the Avro record's `fields` list, which should be identical in
/// ordering since we generated the Avro schema from Arrow).
fn extract_field_inner_schemas(avro_schema: &AvroSchemaDef) -> Vec<AvroSchemaDef> {
    match avro_schema {
        AvroSchemaDef::Record(r) => r
            .fields
            .iter()
            .map(|f| unwrap_nullable_schema(&f.schema))
            .collect(),
        _ => vec![],
    }
}

/// If `schema` is a two-variant nullable union (`["null", T]`), return a
/// clone of the non-null branch.  Otherwise return a clone of `schema` itself.
fn unwrap_nullable_schema(schema: &AvroSchemaDef) -> AvroSchemaDef {
    match schema {
        AvroSchemaDef::Union(u) => u
            .variants()
            .iter()
            .find(|s| !matches!(s, AvroSchemaDef::Null))
            .cloned()
            .unwrap_or(AvroSchemaDef::Null),
        s => s.clone(),
    }
}

